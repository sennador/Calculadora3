public class Calculadora3 {
    //metodo para somar dois numeros.
  public int somar (int a,int b) {
      return a + b;
  }
    //metodo para subtrair dois numeros.
  public int subtrair (int a , int b){
      return a - b;
  }
    //metodo para dividir dois numeros.
  public int dividir (int a , int b){
      return a / b;
  }
    //metodo para multiplicar dois numeros.
  public int multiplicar (int a , int b){
      return a * b;
  }
    //metodo para saber o resto da divisao de dois numeros.
  public int modular (int a , int b){
      return a % b;
  }
}

